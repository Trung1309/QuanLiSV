    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        StringBuilder sb = new StringBuilder();
        if(txtmsv.getText().equals("")){
            sb.append("Mã Sinh viên không dược để trống");
            txtmsv.setBackground(Color.red);
        }else{
            txtmsv.setBackground(Color.white);
        }
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this, sb);
            return;
        }
        try {
            Student stud = new Student();
            stud.setMaSV(txtmsv.getText());
            stud.setHoTen(txtHoTen.getText());
            stud.setEmail(txtEmail.getText());
            stud.setSoDT(txtSoDT.getText());
            stud.setGioiTinh(rdbNam.isSelected()?1:0);
            stud.setDiaChi(txtDiaChi.getText());
            
            StudentDao dao  = new StudentDao();
            dao.update(stud);
            
            JOptionPane.showMessageDialog(this, "Sinh viên đã được cập nhật vào CSDL");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

