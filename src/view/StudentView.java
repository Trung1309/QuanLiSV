    private void setData(List<Student> list){
        for(Student user : list){
            model.addRow(new Object[]{
            user.getMaSV(), user.getHoTen(),user.getEmail(),user.getSoDT(),user.getGioiTinh(),user.getDiaChi()
        });
        }
    }
