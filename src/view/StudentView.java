    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        // TODO add your handling code here:
        if(txtmsv.getText().equals("")){
            JOptionPane.showMessageDialog(this, "Bạn cần nhập mã sinh viên");
            return;
        }
        try {
            StudentDao Dao = new StudentDao();
            Student student = Dao.findByiD(txtmsv.getText());
            
           if(student != null){
               txtHoTen.setText(student.getHoTen());
               txtEmail.setText(student.getEmail());
               txtSoDT.setText(student.getSoDT());
               rdbNam.setSelected(student.getGioiTinh()==1);
               rdbNu.setSelected(student.getGioiTinh()==0);
               txtDiaChi.setText(student.getDiaChi());
           }else{
               JOptionPane.showMessageDialog(this, "Không tìm thấy sinh viên");
           }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnTimKiemActionPerformed

